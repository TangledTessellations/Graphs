def getGraph(request):
    picture = pictures[request][0]
    if len(pictures[request]) > 1:
        color = pictures[request][1]
    else:
        color = None
    graph = Graph()
    current_id = 0
    for part in picture:
        for vertex in range(len(part)):
            graph.add_vertex(vertex_id=current_id, x=part[vertex][0],y=part[vertex][1], color=color)
            if vertex > 0:
                graph.add_edge(current_id - 1, current_id)
            current_id += 1
    return graph